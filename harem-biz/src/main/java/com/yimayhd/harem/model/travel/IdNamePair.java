package com.yimayhd.harem.model.travel;

/**
 * 只包含ID和名称的数据对象
 * 
 * @author yebin
 *
 */
public class IdNamePair {
	private String id;
	private String name;

	
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
